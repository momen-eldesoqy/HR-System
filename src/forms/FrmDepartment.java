    private void clearData()
    {
        Tools.clearText(this);
        txt_id.setText(dept.getAutoNumber());
        btn_add.setEnabled(true);
        btn_update.setEnabled(false);
        btn_del.setEnabled(false);
        txt_name.requestFocus();
        
        
    }
    
    
    private void btn_clearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_clearActionPerformed
        clearData();          
    }//GEN-LAST:event_btn_clearActionPerformed

    
    private void setValues()
    {
        dept.setDept_id(Integer.parseInt(txt_id.getText()));
        dept.setDept_name(txt_name.getText());
        dept.setDept_desc(txt_desc.getText());
    }
    
    
    private void btn_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addActionPerformed
        
        setValues();
        dept.add();
        clearData();
        dept.getAllRows(tbl_dept);
        
        
    }//GEN-LAST:event_btn_addActionPerformed

    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
        
        setValues();
        dept.update();
        clearData();        
        dept.getAllRows(tbl_dept);
        
    }//GEN-LAST:event_btn_updateActionPerformed

    private void btn_delActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_delActionPerformed
       if(Tools.confirmMsg("Do You want To Delete It ?"))
       {
        setValues();
        dept.delete();
        clearData();         
        dept.getAllRows(tbl_dept);  
       }

        
        
    }//GEN-LAST:event_btn_delActionPerformed

