    private void tbl_deptMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_deptMouseClicked
        
        try
        {
            int row = tbl_dept.getSelectedRow();
            String deptID = tbl_dept.getValueAt(row, 0).toString();
            String deptName = tbl_dept.getValueAt(row, 1).toString();
            String deptDesc = tbl_dept.getValueAt(row, 2).toString();
            
            txt_id.setText(deptID);
            txt_name.setText(deptName);
            txt_desc.setText(deptDesc);
            
            btn_add.setEnabled(true);
            btn_update.setEnabled(true);
            btn_del.setEnabled(true);
            
            
        }catch(Exception e)
        {
            Tools.msgBox(e.getMessage());
        }
        
        
        
    }//GEN-LAST:event_tbl_deptMouseClicked

    private void jBTN8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBTN8ActionPerformed
        
        String search = "select * from department_data where  ";
        if(radio_id.isSelected())
        {
            search += "ID ";
        }else if(radio_name.isSelected())
        {
             search += "Department ";
        }else
        {
            Tools.msgBox("error");
        }
        search += "like'%"+txt_search.getText()+"%'";
        dept.getCustomRows(search, tbl_dept);
        
        
        
        
    }//GEN-LAST:event_jBTN8ActionPerformed

