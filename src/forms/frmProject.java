    private void tbl_projMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_projMouseClicked
        
        try
        {
            int row = tbl_proj.getSelectedRow();
            String projID = tbl_proj.getValueAt(row, 0).toString();
            String projName = tbl_proj.getValueAt(row, 1).toString();
            String projDesc = tbl_proj.getValueAt(row, 2).toString();
            String deptName = tbl_proj.getValueAt(row, 4).toString();
            
            txt_id.setText(projID);
            txt_name.setText(projName);
            txt_desc.setText(projDesc);
            jCOMBOBOX1.setSelectedItem(deptName);
            
            btn_add.setEnabled(false);
            btn_update.setEnabled(true);
            btn_delete.setEnabled(true);
            
        }catch(Exception e)
        {
            Tools.msgBox(e.getMessage());
        }
        
        
    }//GEN-LAST:event_tbl_projMouseClicked

