     private void clearData()
    {
        Tools.clearText(this);
        txt_id.setText(p.getAutoNumber());
        btn_add.setEnabled(true);
        btn_update.setEnabled(false);
        btn_delete.setEnabled(false);
        txt_name.requestFocus();
    }
    
    
