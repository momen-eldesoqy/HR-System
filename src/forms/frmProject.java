    private void setValues()
    {
        int pID = Integer.parseInt(txt_id.getText());
        String pName = txt_name.getText();
        String pDesc = txt_desc.getText();
        
        String deptName = jCOMBOBOX1.getSelectedItem().toString();
        
        int depID = Integer.parseInt(dept.getValueByName(deptName));
        
        p.setProject_id(pID);
        p.setProject_name(pName);
        p.setProject_desc(pDesc);
        p.setDept_id(depID);
    }
    
    
